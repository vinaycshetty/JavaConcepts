package com.javaBasics;

public class JavaDataTypes {

	public static void main(String[] args) {
		
		// Primitive data types --- int,double,char,boolean
		// String is a Class and not datatype----imp
		
		int a=9; // int type 1 to infinite and 0 and -1 to -infinite
		System.out.println("Value of a is "+a);
		
		int aa=-2;
		System.out.println("Value of aa is "+aa);
		
		char c = 'V';
		System.out.println("Value of c is "+c);
		
		char cc='2';
		System.out.println("Value of cc is "+cc);
		
		double d = 10.22;
		System.out.println("Value of d is "+d);
		
		double dd= 12;
		System.out.println("Value of dd is "+dd);
		
		boolean x = true;
		boolean y = false;
		
		System.out.println(x);
		System.out.println(y);
	
		String b= "vinay"; // String can store data in ""
		System.out.println("Value of b is "+b);
		
		String bb="100";
		System.out.println("Value of bb is "+bb);
		
		
	}

}
